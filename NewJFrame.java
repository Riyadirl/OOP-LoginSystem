    private void showActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        try{
            BufferedReader reader = new BufferedReader(new FileReader("src/dat.txt"));
            String s;
            while((s = reader.readLine()) != null){
                String[] p = s.split("_");
                ta.append("Email: "+p[0]+"\n"+"Password: "+p[1]+"\n");
            }
            reader.close();
        }
        catch( IOException e){
            e.printStackTrace();
        }
    }

