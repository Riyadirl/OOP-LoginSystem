    private void registerActionPerformed(java.awt.event.ActionEvent evt) {
        String email = emailtf.getText();
        String password = passtf.getText();
        try{
            BufferedWriter writer = new BufferedWriter(new FileWriter("src/dat.txt", true));
            writer.write(email+"_"+password+"\n");
            writer.close();
        }catch(IOException e){
            e.printStackTrace();
        }
        emailtf.setText(null);
        passtf.setText(null);
    }

